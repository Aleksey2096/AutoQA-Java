package pl.traineeship.autoqa.lesson8.phonebook;

/*
Написать простой класс Телефонный Справочник, который хранит в себе список фамилий и телефонных
номеров. В этот телефонный справочник с помощью метода add() можно добавлять записи,
а с помощью метода get() искать номер телефона по фамилии. Следует учесть, что под одной
фамилией может быть несколько телефонов (в случае однофамильцев),
тогда при запросе такой фамилии должны выводиться все телефоны.

Желательно не добавлять лишний функционал (дополнительные поля (имя, отчество, адрес),
взаимодействие с пользователем через консоль и т.д). Консоль использовать только для
вывода результатов проверки телефонного справочника.
 */
public class App {
    public static void main(String[] args) {

        Phonebook phonebook = new Phonebook();

        phonebook.add("Mary", "+1 (555) 234-5678");

        System.out.println(phonebook.get("Mary"));  // [+1 (555) 234-5678]

        phonebook.add("James", "+1 (555) 123-4567");
        phonebook.add("James", "+1 (555) 987-6543");
        phonebook.add("James", "+1 (555) 456-7890");
        phonebook.add("James", "+1 (555) 234-5678");

        System.out.println(phonebook.get("James"));
        // [+1 (555) 456-7890, +1 (555) 987-6543, +1 (555) 234-5678, +1 (555) 123-4567]
    }
}
